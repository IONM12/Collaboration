using System;
using System.Collections.Generic;
///////////////////////////////////////////////////////////////////////////////////////////////////////
using Ion;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Collaboration
{
	public class MENU : MONOBEHAVIOUR
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		TEXTURE MenuTexture = "Menu/Texture/MenuTexture";
		//----------------------------------------------------------------------------------------------------
		bool BoardFlag = false;
		bool GameFlag = false;
		//----------------------------------------------------------------------------------------------------
		GAMEOBJECT Game;
		GAMEOBJECT Board;
		//----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ START
		//====================================================================================================
		void Start()
		{
			Board =  GAMEOBJECT.Find("Board");
			Game = GAMEOBJECT.Find("Game");
		}
		//====================================================================================================
		// ■ UPDATE
		//====================================================================================================
		void Update()
		{
			if (INPUT.GetKeyUp(KEYCODE.F1)) BoardFlag = !BoardFlag;
			if (INPUT.GetKeyUp(KEYCODE.F2)) GameFlag = !GameFlag;

			Board.SetActive(BoardFlag);
			Game.SetActive(GameFlag);
		}
		//====================================================================================================
		// ■ GUI
		//====================================================================================================
		void OnGUI()
		{
			UI.depth = 1;

			UI.DrawTexture(new RECTANGLE(225, 0, SCREEN.width - 450, 70), MenuTexture);


		}
		//====================================================================================================
	}
}
