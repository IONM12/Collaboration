using Unity;
using UnityEngine;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Ion
{
	public class MESHCOLLIDER
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		public MeshCollider MeshCollider;
		//-----------------------------------------------------------------------------------------------------
		public MESH sharedMesh
		{
			get { return new MESH(MeshCollider.sharedMesh); }
			set { MeshCollider.sharedMesh = value.Mesh; }
		}
		//-----------------------------------------------------------------------------------------------------
		public bool convex
		{
			get { return MeshCollider.convex; }
			set { MeshCollider.convex = value; }
		}
		//-----------------------------------------------------------------------------------------------------
		public bool isTrigger
		{
			get { return MeshCollider.isTrigger; }
			set { MeshCollider.isTrigger = value; }
		}
		//-----------------------------------------------------------------------------------------------------

		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ CONSTRUCTOR
		//====================================================================================================
		public MESHCOLLIDER(MeshCollider meshCollider)
		{
			MeshCollider = meshCollider;
		}
		//====================================================================================================
		public static implicit operator MeshCollider(MESHCOLLIDER meshCollider)
		{
			if (meshCollider == null) return null;
			return meshCollider.MeshCollider;
		}
		//====================================================================================================
	}
}
