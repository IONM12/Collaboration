using Unity;
using UnityEngine;
///////////////////////////////////////////////////////////////////////////////////////////////////////
using System;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Ion
{
	[Serializable]
	public class MATERIAL
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		public Material Material;
		//----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ CONSTRUCTOR
		//====================================================================================================
		public MATERIAL()
		{
		}
		//====================================================================================================
		public MATERIAL(SHADER shader)
		{
			shader.LoadShader();
			Material = new Material(shader);
		}
		//====================================================================================================
		public MATERIAL(Material material)
		{
			Material = material;
		}
		//====================================================================================================
		public void SetColor(string name, COLOR color)
		{
			Material.SetColor(name, color);
		}
		//====================================================================================================
		public void SetInt(string name, int value)
		{
			Material.SetInt(name, value);
		}
		//====================================================================================================
		public void SetFloat(string name, float value)
		{
			Material.SetFloat(name, value);
		}
		//====================================================================================================
		public void SetVector(string name, float4 value)
		{
			Material.SetVector(name, value);
		}
		//====================================================================================================
		public void SetVector(string name, float3 value)
		{
			Material.SetVector(name, new float4(value, 1));
		}
		//====================================================================================================
		public void SetVector(string name, float2 value)
		{
			Material.SetVector(name, new float4(value, 0, 1));
		}
		//====================================================================================================
		public void SetMatrix(string name, float4x4 matrix)
		{
			Material.SetMatrix(name, matrix);
		}
		//====================================================================================================
		public void SetTexture(string name, TEXTURE texture)
		{
			Material.SetTexture(name, texture);
		}
		//====================================================================================================
		public void SetTexture(string name, TEXARRAY texArray)
		{
			Material.SetTexture(name, texArray);
		}
		//====================================================================================================
		public void SetBuffer(string name, BUFFER buffer)
		{
			Material.SetBuffer(name, buffer);
		}
		//====================================================================================================
		public void SetTexture(string name, TEXTURE2DARRAY texture)
		{
			Material.SetTexture(name, texture);
		}
		//====================================================================================================
		internal void SetPass()
		{
			Material.SetPass(0);
		}
		//====================================================================================================
		public static implicit operator Material(MATERIAL material)
		{
			return material.Material;
		}
		//====================================================================================================
	}
}
