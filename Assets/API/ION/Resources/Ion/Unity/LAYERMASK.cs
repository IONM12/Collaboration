namespace Ion
{
	public struct LAYERMASK
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------

		//----------------------------------------------------------------------------------------------------

		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		static public int NameToLayer(string name)
		{
			int value = LayerMask.NameToLayer(name);

			if (value == -1)
			{
				Debug.Log("Layer Name not exist : " + name);
			}

			return value;
		}
		//====================================================================================================
		static public string LayerToName(int layer)
		{
			return LayerMask.LayerToName(layer);
		}
		//====================================================================================================
		static public int GetLayerMask(string name)
		{
			int value = NameToLayer(name);

			return (1 << value);
		}
		//====================================================================================================
		static public int GetAllLayerButThis(string name)
		{
			int value = NameToLayer(name);

			return ~(1 << value);
		}
		//====================================================================================================
		static public int GetAllLayer()
		{
			return -1;
		}
		//====================================================================================================
	}
}
