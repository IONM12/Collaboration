using Unity;
using UnityEngine;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Ion
{
	public static class EVENT
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		static public float2 mousePosition
		{
			get { return Event.current.mousePosition; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public Event current
		{
			get { return Event.current; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public EVENTTYPE type
		{
			get { return (EVENTTYPE)Event.current.type; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public float2 delta
		{
			get { return Event.current.delta; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public int button
		{
			get { return Event.current.button; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public KEYCODE keyCode
		{
			get { return (KEYCODE)Event.current.keyCode; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public bool control
		{
			get { return Event.current.control; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public bool alt
		{
			get { return Event.current.alt; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public bool shift
		{
			get { return Event.current.shift; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public bool capsLock
		{
			get { return Event.current.capsLock; }
		}
		//-----------------------------------------------------------------------------------------------------
		static public bool repaint
		{
			get
			{
				if (Event.current.type == EventType.Repaint) return true;
				else return false;
			}
		}
		//-----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		static public int GetControlID()
		{
			return GUIUtility.GetControlID(FocusType.Passive);
		}
		//====================================================================================================
		static public EVENTTYPE GetTypeForControl(int controlID)
		{
			return (EVENTTYPE)Event.current.GetTypeForControl(controlID);
		}
		//====================================================================================================
		static public void Use()
		{
			Event.current.Use();
		}
		//====================================================================================================
	}
}
