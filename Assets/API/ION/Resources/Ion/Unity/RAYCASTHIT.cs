using Unity;
using UnityEngine;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Ion
{
	public class RAYCASTHIT
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		bool Hit;
		//-----------------------------------------------------------------------------------------------------
		RaycastHit RaycastHit;
		//-----------------------------------------------------------------------------------------------------
		public bool hit
		{
			get { return Hit; }
			set { Hit = value; }
		}
		//-----------------------------------------------------------------------------------------------------
		public float3 point
		{
			get { return RaycastHit.point; }
			set { RaycastHit.point = value; }
		}
		//-----------------------------------------------------------------------------------------------------
		public float3 normal
		{
			get { return RaycastHit.normal; }
			set { RaycastHit.normal = value; }
		}
		//-----------------------------------------------------------------------------------------------------
		public COLLIDER collider
		{
			get { return new COLLIDER(RaycastHit.collider); }
		}
		//-----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ CONSTRUCTOR
		//====================================================================================================
		internal RAYCASTHIT(bool hit, RaycastHit raycastHit)
		{
			Hit = hit;
			RaycastHit = raycastHit;
		}
		//====================================================================================================
	}
}
