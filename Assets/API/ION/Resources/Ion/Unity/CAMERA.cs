using Unity;
using UnityEngine;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Ion
{
	public class CAMERA
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		Camera Camera;
		//----------------------------------------------------------------------------------------------------
		public float4x4 worldToCameraMatrix
		{
			get { return Camera.worldToCameraMatrix; }
		}
		//-----------------------------------------------------------------------------------------------------
		public float4x4 projectionMatrix
		{
			get { return Camera.projectionMatrix; }
		}
		//-----------------------------------------------------------------------------------------------------
		public float3 WorldToScreenPoint(float3 point)
		{
			return Camera.WorldToScreenPoint(point);
		}
		//-----------------------------------------------------------------------------------------------------
		public RAY ViewportPointToRay(float3 point)
		{
			return new RAY(Camera.ViewportPointToRay(point));
		}
		//----------------------------------------------------------------------------------------------------
		static public CAMERA current
		{
			get { return new CAMERA(Camera.current); }
		}
		//-----------------------------------------------------------------------------------------------------
		public TRANSFORM transform
		{
			get { return new TRANSFORM(Camera.transform); }
		}
		//-----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ CONSTRUCTOR
		//====================================================================================================
		public CAMERA()
		{
			Camera = new Camera();
		}
		//====================================================================================================
		internal CAMERA(Camera camera)
		{
			Camera = camera;
		}
		//====================================================================================================
		public static implicit operator Camera(CAMERA camera)
		{
			return camera.Camera;
		}
		//====================================================================================================
	}
}
