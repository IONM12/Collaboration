using System;
///////////////////////////////////////////////////////////////////////////////////////////////////////
using Unity;
using UnityEngine;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Ion
{
	[Serializable]
	public class TEXTURE
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		public Texture Texture;
		//-----------------------------------------------------------------------------------------------------
		string FileName;
		//-----------------------------------------------------------------------------------------------------
		public int width
		{
			get
			{
				if (Texture == null) return 0;
				return Texture.width;
			}
		}
		//-----------------------------------------------------------------------------------------------------
		public int height
		{
			get
			{
				if (Texture == null) return 0;
				return Texture.height;
			}
		}
		//-----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ CONSTRUCTOR
		//====================================================================================================
		public TEXTURE(Texture texture)
		{
			Texture = texture;
		}
		//====================================================================================================
		public TEXTURE(string fileName)
		{
			FileName = fileName;
		}
		//====================================================================================================
		Texture GetTexture()
		{
			if (Texture == null)
			{
				Load();
			}

			return Texture;
		}
		//====================================================================================================
		void Load()
		{
			Texture = Resources.Load<Texture>(FileName);

			if (Texture == null)
			{
				Debug.Log("ERROR, Cant load texture file : " + FileName);
				Texture = Texture2D.whiteTexture;
			}
		}
		//====================================================================================================
		public static implicit operator Texture(TEXTURE texture)
		{
			return texture.GetTexture();
		}
		//====================================================================================================
		public static implicit operator TEXTURE(string fileName)
		{
			return new TEXTURE(fileName);
		}
		//====================================================================================================
	}
}
