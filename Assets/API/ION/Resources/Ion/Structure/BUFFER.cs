namespace Ion
{
	public class BUFFER
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		internal ComputeBuffer Buffer;
		//----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ CONSTRUCTOR
		//====================================================================================================
		internal BUFFER(int count, int stride)
		{
			Buffer = new ComputeBuffer(count, stride);
		}
		//====================================================================================================
		internal void Release()
		{
			if (Buffer != null)
			{
				Buffer.Release();
				Buffer = null;
			}
		}
		//====================================================================================================
		public void GetData(Array array)
		{
			Buffer.GetData(array);
		}
		//====================================================================================================
		public void SetData(Array array)
		{
			Buffer.SetData(array);
		}
		//====================================================================================================
		public void GetData(Array array, int arrayIndex, int bufferIndex, int count)
		{
			Buffer.GetData(array, arrayIndex, bufferIndex, count);
		}
		//====================================================================================================
		public void SetData(Array array, int arrayIndex, int bufferIndex, int count)
		{
			Buffer.SetData(array, arrayIndex, bufferIndex, count);
		}
		//====================================================================================================
		public static implicit operator ComputeBuffer(BUFFER buffer)
		{
			if (buffer.Buffer == null) return null;
			return buffer.Buffer;
		}
		//====================================================================================================
	}
}
