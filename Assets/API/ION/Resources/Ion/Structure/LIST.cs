using System;
using System.Collections.Generic;
using System.Runtime.InteropServices;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Ion
{
	[System.Serializable]
	public class LIST<T>
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		List<T> List;
		//----------------------------------------------------------------------------------------------------
		public int Count { get { return List.Count; } }
		//----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■

		//====================================================================================================
		// ■ 
		//====================================================================================================
		public LIST()
		{
			List = new List<T>();
		}
		//====================================================================================================
		// ■ 
		//====================================================================================================
		public void Add(T item)
		{
			List.Add(item);
		}
		//---------------------------------------------------------------------------------------------
		public void RemoveAt(int index)
		{
			List.RemoveAt(index);
		}
		//---------------------------------------------------------------------------------------------
		public void Remove(T item)
		{
			List.Remove(item);
		}
		//---------------------------------------------------------------------------------------------
		public void Clear()
		{
			List.Clear();
		}
		//---------------------------------------------------------------------------------------------
		public T[] ToArray()
		{
			return List.ToArray();
		}
		//---------------------------------------------------------------------------------------------
		public T this[int i]
		{
			get { return List[i]; }
			set { List[i] = value; }
		}
		//---------------------------------------------------------------------------------------------
	}
}
